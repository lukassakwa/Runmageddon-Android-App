package com.olivier.jasmedapp.model;

public class Mail {
    private String title;
    private String body;

    public Mail() {
    }

    public Mail(String title, String body) {
        this.title = title;
        this.body = body;
    }

    public String getTitle() {
        return title;
    }

    public void setTitle(String title) {
        this.title = title;
    }

    public String getBody() {
        return body;
    }

    public void setBody(String body) {
        this.body = body;
    }
}
